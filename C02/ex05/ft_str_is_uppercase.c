int	ft_str_is_uppercase(char *str)
{
	while (*str != '\0')
	{
		if (!(*str >= 'A' && *str <= 'Z'))
			return (0);
		else if (*str == '\0')
			return (1);
		str++;
	}
	return (1);
}
